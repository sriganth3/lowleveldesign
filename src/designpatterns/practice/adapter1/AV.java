package designpatterns.practice.adapter1;

public class AV {

	public String audio;
	public String video;
	
	public AV(String audio, String video) {
		super();
		this.audio = audio;
		this.video = video;
	}
	public String getAudio() {
		return audio;
	}
	public String getVideo() {
		return video;
	}
	
	
}
